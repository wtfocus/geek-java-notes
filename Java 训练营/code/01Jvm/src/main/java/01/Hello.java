

public class Hello {
    public static void main(String[] args) {
        byte n1 = 1;
        int n2 = 2;
        long n3 = 3L;
        double n4 = 4.0D;

        if (2 < n3) {
            System.out.println("n2 + n3 = " + n2 + n3);
        }

        for (double i = 0; i <= n4; i++) {
            System.out.println("n1 + i = " + n1 + i);
        }
    }
}
